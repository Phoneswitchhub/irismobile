// 현재 설정된 언어 (기본값: 태국어 th)
let currentLang = localStorage.getItem("iris_lang") || "th";

// 언어 변경 및 저장
function changeLanguage(lang) {
  if (!TRANSLATIONS[lang]) return;
  currentLang = lang;
  localStorage.setItem("iris_lang", lang);
  
  // HTML lang 속성 업데이트
  document.documentElement.lang = lang;
  
  // 다국어 적용 실행
  applyTranslations();
  
  // 언어 변경 이벤트 디스패치 (각 페이지별 커스텀 재렌더링 대응)
  window.dispatchEvent(new CustomEvent("languageChanged", { detail: { lang } }));
}

// DOM 엘리먼트에 다국어 적용
function applyTranslations() {
  const elements = document.querySelectorAll("[data-i18n]");
  elements.forEach(el => {
    const key = el.getAttribute("data-i18n");
    const trans = TRANSLATIONS[currentLang][key];
    if (trans) {
      // placeholder 지원 여부 체크
      if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
        el.setAttribute("placeholder", trans);
      } else {
        el.innerHTML = trans;
      }
    }
  });
  
  // 다국어 선택기 UI 업데이트
  const select = document.getElementById("langSelect");
  if (select) {
    select.value = currentLang;
  }
}

// 텍스트 변환 헬퍼 (스크립트 내부용)
function t(key) {
  return TRANSLATIONS[currentLang][key] || key;
}

// 언어 표시용 국기/텍스트
function getLangFlag(lang) {
  const flags = { th: "🇹🇭 TH", mm: "🇲🇲 MM", ko: "🇰🇷 KO" };
  return flags[lang] || lang.toUpperCase();
}

// 로드 시 즉시 자동 적용 (DOM 완료 상태 대응)
if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", applyTranslations);
} else {
  applyTranslations();
}
